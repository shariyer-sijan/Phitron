#include <bits/stdc++.h>
using namespace std;
char grid[105][105] ;
bool vis[105][105] ;
int level[105][105] ;
int n,m ;
bool valid(int i, int j ){
    if(i<0 || i>=n || j<0 || j>=m ){
        return false ;
    }
    return true ;
}
vector<pair<int,int>> d={{2,1},{1,2},{-2,1},{-1,2},{-1,-2},{-2,-1},{1,-2},{2,-1}} ;

void bfs(int si , int sj) {
    queue<pair<int,int>> q ;
    q.push({si,sj} ) ;
    vis[si][sj]=true ;
    level[si][sj]=0 ;
    while( !q.empty() ){
        pair<int,int> par=q.front() ;
        q.pop() ;
        int par_i=par.first ;
        int par_j=par.second ;
        for(int i=0 ; i<8 ; i++){
        int ci= par_i+d[i].first ;
        int cj= par_j+ d[i].second ;

        if(valid(ci,cj) && !vis[ci][cj] ){
            q.push({ci,cj}) ;
            level[ci][cj]=level[par_i][par_j] +1 ;
            vis[ci][cj]=true ;
        }
    }

    }
    
}
int main() {

    int t;
    cin>>t ;
    while(t--){
        cin>>n>>m  ;
    for(int i=0 ; i<n ; i++){
        for(int j=0 ; j<m ; j++){
         grid[i][j]= 1 ;
        }
    }
    int si,sj , di,dj ;
    cin>>si>>sj>>di>>dj ;
    memset(vis,false,sizeof(vis)) ;
    memset(level,-1,sizeof(level)) ;
    bfs(si,sj) ;
    cout<<level[di][dj]<<endl ;
    }
    return 0;
}